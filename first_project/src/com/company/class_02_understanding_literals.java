package com.company;

public class class_02_understanding_literals {
    public static void main(String[] args){
        System.out.println("Hello world This is aadarsh");
        byte age=34; //byte lies between -127 to 127 inclusive
        int a=10;
        double b=20.34; //Double can store 64 bits
        double c=34.56D; //You don't compulsorily need to put D to denote double
        float d=23.4f; //Float can store 32 bits
        char e='A';
        long f=78787L;
        long g=2323l; //Both capital and small "l or L" can be used to denote long data type
        short h=45; //Short can store only 16 bits (-32,768 to 32, 768)
        boolean i=true;
        String j="Aadarsh";
        /*
        * System.out.println(); Prints in new line
        * System.put.print() continues printing in same line
        */
        System.out.println(j);

    }
}
